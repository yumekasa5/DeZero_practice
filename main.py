#!/usr/bin/env python3
# -*- coding: utf-8 -*-
import numpy as np

from Variable import Variable
from Function import Function, Square, Exp

A = Square()
B = Exp()
C = Square()

x = Variable(np.array(0.5))
a = A(x)
b = B(a)
y = C(b)
print(y.data)

# Backward pass
y.grad = np.array(1.0)
b.grad = C.backward(y.grad)
a.grad = B.backward(b.grad)
x.grad = A.backward(a.grad)
print(x.grad) 